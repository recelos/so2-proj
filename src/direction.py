class Direction:
    def __init__(self, direction_name, x1, x2, y1, y2, x_speed, y_speed, orientation):
        self.direction_name = direction_name
        self.x1 = x1
        self.x2 = x2
        self.y1 = y1
        self.y2 = y2
        self.x_speed = x_speed
        self.y_speed = y_speed
        self.orientation = orientation